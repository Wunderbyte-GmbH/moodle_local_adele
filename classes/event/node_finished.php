namespace local_adele\event;

use html_writer;
use moodle_url;

/**
 * The learnpath created event class.
 *
 * @package     local_adele
 * @author      Jacob Viertel
 * @copyright  2023 Wunderbyte GmbH
 * @license    http://www.gnu.org/copyleft/gpl.html GNU GPL v3 or later
 */
class node_finished extends \core\event\base {

    /**
     * Init parameters.
     *
     * @return void
     *
     */
    protected function init() {
        $this->data['crud'] = 'u';
        $this->data['edulevel'] = self::LEVEL_OTHER;
        $this->data['objecttable'] = 'local_adele_path_user';
    }

    /**
     * Get name.
     *
     * @return string
     *
     */
    public static function get_name() {
        return get_string('event_node_finished', 'local_adele');
    }

    /**
     * Get description.
     *
     * @return string
     *
     */
    public function get_description() {
        $data = $this->data;
        $names = [];
        foreach ($data['other']['node'] as $singelnode) {
            $names[] = $singelnode['data']['fullname'];
        }
        return get_string('event_node_finished_description', 'local_adele', [
            'user' => $data['other']['user']['userpath']['user_id'] ?? 'missing',
            'node' => implode(', ' , $names) ?? 'missing',
        ]);
    }

    /**
     * Get url.
     *
     * @return object
     *
     */
    public function get_url() {
        return new moodle_url('');
    }
}
